static const uint8_t ICON_BROWSER_16[] = {
    0x00,0x00, 0x7F,0xFE, 0x40,0x02, 0x5F,0xFA, 0x50,0x0A, 0x5F,0xFA,
    0x50,0x0A, 0x5F,0xFA, 0x50,0x0A, 0x5F,0xFA, 0x50,0x0A, 0x5F,0xFA,
    0x40,0x02, 0x7F,0xFE, 0x00,0x00, 0x00,0x00
};

static const uint8_t *icon_bitmaps[ICON_COUNT] = {
    ICON_SETTINGS_16,
    ICON_DIALER_16,
    ICON_MESSAGING_16,
    ICON_BROWSER_16,
};

void icon_draw(canvas_t *c, icon_id_t id, int32_t cx, int32_t cy, uint32_t fg, uint32_t bg) {
    if (!c || id < 0 || id >= ICON_COUNT) return;

    int32_t scale = ICON_DISPLAY_SIZE / ICON_SRC_SIZE;
    int32_t x = cx - ICON_DISPLAY_SIZE / 2;
    int32_t y = cy - ICON_DISPLAY_SIZE / 2;

    canvas_draw_rounded_rect(c, x, y, ICON_DISPLAY_SIZE, ICON_DISPLAY_SIZE,
                             12, bg, true);
    canvas_draw_bitmap_mono_scaled(c, x + 4, y + 4, ICON_SRC_SIZE, ICON_SRC_SIZE,
                                   scale - 1, icon_bitmaps[id], fg, true);
}
